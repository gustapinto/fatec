public class Funcoes {
    public int somar() {
        int n1 = 0;
        int n2 = 2;
        
        return n1 + n2;
    }
    
    public int somar(int n1, int n2) {
        return n1 + n2;
    }
    
    public void subtrair() {
        int n1 = 3;
        int n2 = 1;
        
        System.out.println("Sub: " + (n1 - n2));
    }
}
